def magic_calculation(a, b):
    result = 0  # load_const
    for i in range(1, 3):  # loop for,stofast load glob,const
        try:  # set excep
            if i > a:  # pop if,loafas,compa,loafast
                raise Exception('Too far')  # raisevarg loaglo,lconst
            else:
                result += (a**b)/i  # lfas3,binatruediv
        except:  # pop_top
            result = b + a  # storfas,lfas,binadd
            break  # break_loop
    return result  # return, loafast
